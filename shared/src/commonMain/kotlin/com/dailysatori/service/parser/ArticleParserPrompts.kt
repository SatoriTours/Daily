    输出要求：
    1. 只输出摘要正文，不要输出“标题”“核心内容”“核心观点”等引导标题，也不要重复文章标题。
    2. 只基于原文内容，不要编造信息，禁止补充原文没有的背景、动机、情节或评价。
    3. 内容很短时只做忠实翻译或轻量整理，保留原意和语气，不要扩写成新闻稿或故事。
    4. 内容较长时，用 1 段简短摘要加少量要点即可；能不用列表就不用列表。
